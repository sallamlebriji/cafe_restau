import i18n from "i18next";
import { initReactI18next } from "react-i18next";

export const supportedLanguages = ["fr", "en", "ar"];

export const normalizeLanguage = (language) =>
  supportedLanguages.includes(language) ? language : "fr";

export const getStoredLanguage = () =>
  normalizeLanguage(localStorage.getItem("restaurant-saas-language") || "fr");

export const applyDocumentLanguage = (language) => {
  const nextLanguage = normalizeLanguage(language);
  document.documentElement.lang = nextLanguage;
  document.documentElement.dir = nextLanguage === "ar" ? "rtl" : "ltr";
  localStorage.setItem("restaurant-saas-language", nextLanguage);
};

export const changeAppLanguage = (language) => {
  const nextLanguage = normalizeLanguage(language);
  applyDocumentLanguage(nextLanguage);
  i18n.changeLanguage(nextLanguage);
  return nextLanguage;
};

